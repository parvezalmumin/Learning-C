//Percentage of five numbers
#include<stdio.h>
int main(){
    float marks01 = 89;
    float marks02 = 91;
    float marks03 = 85;
    float marks04 = 79;
    float marks05 = 96;
    float percentage = (marks01 + marks02 + marks03 + marks04 + marks05)/5;
    printf("Percentage of Fice Numbers: %f",percentage);
    return 0;
}